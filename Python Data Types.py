#It IS possible to do maths with floats and integers

#It is NOT possible to do maths with strings or characters

#e.g:

num1 = 20 #int
num2 = 30 #int
total = num1 + num2
print(total)#prints: 50

num3 = 12.3 #float
num4 = 93.2 #float
total2 = num3 + num4
print(total2)#prints: 105.5

#You cannot add strings or characters
#Instead, the add symbol glues them together
#But we do not call it glueing, we call it CONCATENATION

#e.g:

num5 = "5" #string
num6 = "88" #string
total3 = num5 + num6
print(total3) #prints: 588

